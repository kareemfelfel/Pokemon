public class Attacks {
    //Field variables
    private String Name;
    private int PowerPoints;

    //GETTERS AND SETTERS
    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public int getPowerPoints() {
        return PowerPoints;
    }

    public void setPowerPoints(int powerPoints) {
        PowerPoints = powerPoints;
    }

}
